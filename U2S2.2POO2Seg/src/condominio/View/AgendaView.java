    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        if (jTable2.getSelectedRow() != -1) {
            txtId.setText(jTable2.getValueAt(jTable2.getSelectedRow(),0).toString());
            txtData.setText(jTable2.getValueAt(jTable2.getSelectedRow(),1).toString());
            txt_Id_morador.setText(jTable2.getValueAt(jTable2.getSelectedRow(),2).toString());
        }
    }//GEN-LAST:event_jTable2MouseClicked

    private void jbGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbGravarActionPerformed
        String data = txtData.getText().toString().trim();
        int id_numero = Integer.parseInt(txt_Id_morador.getText().toString().trim());
       

        Agenda ag = new Agenda();
        ag.setData(data);
        ag.setId_morador(id_numero);
        

        DAOAgenda daoInserir = new DAOAgenda();
        daoInserir.inserir(ag);
        LerTabela();
        limpa();

    }//GEN-LAST:event_jbGravarActionPerformed

