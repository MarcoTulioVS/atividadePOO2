     public void LerTabela(){
       DefaultTableModel modelo = (DefaultTableModel) jTable2.getModel();
       modelo.setNumRows(0);
       DAOAgenda daoaG = new DAOAgenda();
       for(Agenda ag: daoaG.lista()){
       modelo.addRow(new Object[]{
         ag.getId(),
         ag.getData(),
         ag.getId_morador()
       });
       }
       
       }

