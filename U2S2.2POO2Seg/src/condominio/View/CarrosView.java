     public void LerTabela(){
       DefaultTableModel modelo = (DefaultTableModel) jTable4.getModel();
       modelo.setNumRows(0);
       DAOCarros daoCr = new DAOCarros();
       for(Carros cr: daoCr.lista()){
       modelo.addRow(new Object[]{
         cr.getId(),
         cr.getMarca(),
         cr.getModelo(),
         cr.getPlaca(),
         cr.getCor(),
         cr.getId_apartamento()  
       });
       }
       
       }
     
     
