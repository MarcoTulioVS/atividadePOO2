       String marca = txtMarca.getText().toString().trim();
       String modelo = txtModelo.getText().toString().trim();
       String placa = txtPlaca.getText().toString().trim();
       String cor = txtCor.getText().toString().trim();
       int id_apartamento = Integer.parseInt(txtId_apartamento.getText().toString().trim());
       
       Carros cars = new Carros();
       cars.setMarca(marca);
       cars.setModelo(modelo);
       cars.setPlaca(placa);
       cars.setCor(cor);
       cars.setId_apartamento(id_apartamento);
       
       DAOCarros daoInserir = new DAOCarros();
